int main() {
    // 测试八进制和十六进制的混合运算
    int a = 0123 + 0x456;     // 83 + 1110 = 1193
    int b = 0777 * 0XABC;     // 511 * 2748 = 1404228
    
    // 测试复杂表达式
    int c = -a * b;           // -1193 * 1404228 = -1677242004
    int d = b / a;            // 1404228 / 1193 = 1177
    int e = b % a;            // 1404228 % 1193 = 7
    
    // 测试嵌套表达式
    int f = -(a + b) * (c - d);   // -(1193 + 1404228) * (-1677242004 - 1177)
    int g = (a * b) / (c + d);    // (1193 * 1404228) / (-1677242004 + 1177)
    int h = (a + b) % (c - d);    // (1193 + 1404228) % (-1677242004 - 1177)
    
    // 测试多级运算
    int i = -f + g * h;       // -f + (g * h)
    int j = f / g - h;        // (f / g) - h
    int k = f % g + h;        // (f % g) + h
    
    return i + j + k;         // 返回最终结果
} 