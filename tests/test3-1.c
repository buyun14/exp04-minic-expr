int main() {
    // 测试八进制数字
    int a = 0123;    // 八进制123 = 十进制83
    int b = 0777;    // 八进制777 = 十进制511
    
    // 测试十六进制数字
    int c = 0x123;   // 十六进制123 = 十进制291
    int d = 0XABC;   // 十六进制ABC = 十进制2748
    
    // 测试混合运算
    int e = a + b;   // 83 + 511 = 594
    int f = c - d;   // 291 - 2748 = -2457
    
    return e + f;    // 594 + (-2457) = -1863
} 