int main() {
    // 测试零值
    int a = 0;
    int b = -0;
    
    // 测试最大最小值
    int c = 0x7FFFFFFF;    // 最大32位有符号整数
    int d = 0x80000000;    // 最小32位有符号整数
    
    // 测试除以零（应该返回0）
    int e = 100 / 0;
    
    // 测试取模零（应该返回0）
    int f = 100 % 0;
    
    // 测试溢出
    int g = c + 1;         // 应该溢出
    int h = d - 1;         // 应该溢出
    
    // 测试复杂边界条件
    int i = -c * -1;       // 应该溢出
    int j = d / -1;        // 应该溢出
    int k = c % -1;        // 应该为0
    
    return a + b + e + f + k;  // 应该返回0
} 