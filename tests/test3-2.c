int main() {
    // 测试乘法
    int a = 12 * 5;      // 60
    int b = -8 * 3;      // -24
    
    // 测试除法
    int c = 100 / 4;     // 25
    int d = -50 / 5;     // -10
    
    // 测试取模
    int e = 17 % 5;      // 2
    int f = -17 % 5;     // -2
    
    // 测试取负
    int g = -a;          // -60
    int h = -(-b);       // 24
    
    // 测试复杂表达式
    int i = a * b + c;   // 60 * (-24) + 25 = -1415
    int j = d / e + f;   // -10 / 2 + (-2) = -7
    int k = g % h;       // -60 % 24 = -12
    
    return i + j + k;    // -1415 + (-7) + (-12) = -1434
} 